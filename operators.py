# Operators

num1 = 10

num_2 = 20

# Arithmetic Operators

print("num1 + num2 is ", num1+num_2)
print("num1 - num2 is ", num1-num_2)
print("num1 * num2 is ", num1*num_2)
print("num1 / num2 is ", num1/num_2)
print("num1 // num2 is ", num1 // num_2)
print("num1 ** num2 is ", num1 ** num_2)
print("num1 % num2 is ", num1 % num_2)


# Assignment Operators

a = 4
a += 2  # a + 2 = 6
print(a)
a -= 1   # a - 1 = 5
print(a)
a *= 2  # 5 ^ 2
print(a)
a /= 2  # 10 / 2 = 5.0
print(a)

a //= 2  # 5 // 2 = 2.5 => 2

print(a)

